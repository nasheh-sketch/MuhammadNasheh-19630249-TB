    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // TODO add your handling code here:
        int i = tPegawai.getSelectedRow();
        if(i>=0){
            TableModel model = tPegawai.getModel();
            pegawai = new Pegawai();
            pegawai.setNo(Integer.parseInt(model.getValueAt(i, 0).toString()));
            pegawai.setNip(model.getValueAt(i, 1).toString());
            pegawai.setNamaPegawai(model.getValueAt(i, 2).toString());
            pegawai.setPangkatGolongan(model.getValueAt(i, 3).toString());
            pegawai.setJenisKelamin(model.getValueAt(i, 4).toString());
            pegawai.setAlamat(model.getValueAt(i, 5).toString());
            pegawai.setAgama(model.getValueAt(i, 6).toString());
            pegawai.setGaji(model.getValueAt(i, 7).toString());
            
            TambahPegawai tambahPegawai = new TambahPegawai(pegawai);
            tambahPegawai.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "pilih data yang ingin diubah");
        }
    }//GEN-LAST:event_bUbahActionPerformed

