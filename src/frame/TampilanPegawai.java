    public ArrayList<Pegawai> getPegawaiList(String keyword){
        ArrayList<Pegawai> pegawaiList = new ArrayList<Pegawai>();
        Koneksi koneksi = new Koneksi();
        Connection connection = koneksi.getConnection();
        
        String query = "SELECT * FROM pegawai "+keyword;
        Statement st;
        ResultSet rs;
        
        try{
            st = connection.createStatement();
            rs = st.executeQuery(query);
            while(rs.next()){
                pegawai = new Pegawai(
                        rs.getInt("no"),
                        rs.getString("nip"),
                        rs.getString("nama_pegawai"),
                        rs.getString("pangkat_golongan"),
                        rs.getString("jenis_kelamin"),
                        rs.getString("alamat"),
                        rs.getString("agama"),
                        rs.getString("gaji"));
                pegawaiList.add(pegawai);
            }
        }catch (SQLException | NullPointerException ex) {
            System.err.println("Koneksi Null Gagal");
        }
        return pegawaiList;
    }
    
    public void selectPegawai(String keyword){
        ArrayList<Pegawai> list = getPegawaiList(keyword);
        DefaultTableModel model = (DefaultTableModel)tPegawai.getModel();
        Object[] row = new Object[8];
        
        for (int i=0; i<list.size(); i++){
            row[0] = list.get(i).getNo();
            row[1] = list.get(i).getNip();
            row[2] = list.get(i).getNamaPegawai();
            row[3] = list.get(i).getPangkatGolongan();
            row[4] = list.get(i).getJenisKelamin();
            row[5] = list.get(i).getAlamat();
            row[6] = list.get(i).getAgama();
            row[7] = list.get(i).getGaji();
            
            model.addRow(row);
        }
    }
    
    public final void resetTable(String keyword){
        DefaultTableModel model = (DefaultTableModel)tPegawai.getModel();
        model.setRowCount(0);
        selectPegawai(keyword);
    }
    
    

